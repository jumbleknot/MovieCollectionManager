/**
 * Expo config plugin (feature 023) — permit cleartext HTTP ONLY to the local dev/E2E backends.
 *
 * Android targetSdk >= 28 blocks cleartext (http://) traffic by default; Expo only relaxes this in
 * the DEBUG manifest, so the standalone RELEASE APK the mobile E2E suite installs cannot reach the
 * dev BFF at http://localhost:8082 (reached via `adb reverse`) — the login POST fails with
 * ERR_NETWORK and never leaves the device. (Chrome loads http://localhost:8099 fine because Chrome
 * has its own cleartext policy; the app's own network stack does not.)
 *
 * Rather than enable cleartext globally (`usesCleartextTraffic=true`), install a network security
 * config that permits cleartext ONLY for localhost / 127.0.0.1 / 10.0.2.2 (the emulator host alias).
 * All other domains stay cleartext-blocked, so a production HTTPS build is unaffected — prod talks
 * to its real https:// host, which is NOT in this allowlist and therefore still rejects cleartext.
 *
 * android/ is gitignored (regenerated by `expo prebuild --clean`), so this runs at prebuild time:
 *   1. write res/xml/network_security_config.xml
 *   2. point <application android:networkSecurityConfig="@xml/network_security_config">
 */
const { withAndroidManifest, withDangerousMod, AndroidConfig } = require('@expo/config-plugins');
const fs = require('node:fs');
const path = require('node:path');

const NETWORK_SECURITY_CONFIG = `<?xml version="1.0" encoding="utf-8"?>
<!-- GENERATED by plugins/with-dev-cleartext.js (feature 023). Cleartext permitted ONLY for the
     local dev/E2E backends; every other host stays cleartext-blocked (production HTTPS unaffected). -->
<network-security-config>
  <domain-config cleartextTrafficPermitted="true">
    <domain includeSubdomains="false">localhost</domain>
    <domain includeSubdomains="false">127.0.0.1</domain>
    <domain includeSubdomains="false">10.0.2.2</domain>
  </domain-config>
</network-security-config>
`;

/** @param {import('@expo/config-plugins').ConfigPlugin} */
const withDevCleartext = (config) => {
  config = withDangerousMod(config, [
    'android',
    async (cfg) => {
      const xmlDir = path.join(cfg.modRequest.platformProjectRoot, 'app', 'src', 'main', 'res', 'xml');
      fs.mkdirSync(xmlDir, { recursive: true });
      fs.writeFileSync(path.join(xmlDir, 'network_security_config.xml'), NETWORK_SECURITY_CONFIG, 'utf8');
      return cfg;
    },
  ]);

  config = withAndroidManifest(config, (cfg) => {
    const application = AndroidConfig.Manifest.getMainApplicationOrThrow(cfg.modResults);
    application.$['android:networkSecurityConfig'] = '@xml/network_security_config';
    return cfg;
  });

  return config;
};

module.exports = withDevCleartext;
